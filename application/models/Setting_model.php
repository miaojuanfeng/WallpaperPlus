	function update($data = array())
	{
		$data['setting_modify'] = date('Y-m-d H:i:s');
		$data = get_array_prefix('setting_', $data);
		$this->db->where('setting_id', $data['setting_id']);
		$thisResult = $this->db->update('setting', $data); 

		$log_SQL = $this->session->userdata('log_SQL');
		$log_SQL[] = array(
			'result' => $thisResult,
			'sql' => $this->db->last_query()
		);
		$this->session->set_userdata('log_SQL', $log_SQL);
	}

